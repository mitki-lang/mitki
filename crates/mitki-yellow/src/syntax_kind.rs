use std::fmt::Display;

#[allow(non_camel_case_types)]
#[derive(Clone, Copy, Debug, Eq, Hash, PartialEq)]
pub enum SyntaxKind {
    LEFT_PAREN,
    RIGHT_PAREN,
    LEFT_BRACKET,
    RIGHT_BRACKET,
    LEFT_BRACE,
    RIGHT_BRACE,
    EQ,
    DOT,
    COMMA,
    COLON,
    SEMICOLON,
    FUN_KW,
    IF_KW,
    ELSE_KW,
    LOOP_KW,
    VAL_KW,
    WHILE_KW,
    RETURN_KW,
    BREAK_KW,
    NAME,

    INT_NUMBER,
    FLOAT_NUMBER,
    BINARY_OPERATOR,
    POSTFIX_OPERATOR,
    PREFIX_OPERATOR,

    UNKNOWN,
    EOF,

    BINARY_EXPR,
    ARG_LIST,
    LOOP_EXPR,
    IF_EXPR,
    ERROR,
    EXPR_STMT,
    FIELD_EXPR,
    FN,
    RETURN_TYPE,
    IDENT,
    TYPE_PARAM,
    LITERAL,
    TUPLE_EXPR,
    ARRAY_EXPR,
    MODULE,
    PARAM,
    PARAM_LIST,
    GENERIC_PARAM_LIST,
    PAREN_EXPR,
    POSTFIX_EXPR,
    PATH_TYPE,
    PREFIX_EXPR,
    CALL_EXPR,
    INDEX_EXPR,
    STMT_LIST,
    VAL_STMT,
    RETURN_STMT,
    TOMBSTONE,
}

impl Display for SyntaxKind {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(match self {
            SyntaxKind::LEFT_PAREN => "`(`",
            SyntaxKind::RIGHT_PAREN => "`)`",
            SyntaxKind::LEFT_BRACKET => "`[`",
            SyntaxKind::RIGHT_BRACKET => "`]`",
            SyntaxKind::LEFT_BRACE => "`{`",
            SyntaxKind::RIGHT_BRACE => "`}`",
            SyntaxKind::EQ => "`=`",
            SyntaxKind::DOT => "`.`",
            SyntaxKind::COMMA => "`,`",
            SyntaxKind::COLON => "`:`",
            SyntaxKind::SEMICOLON => "`;`",
            SyntaxKind::FUN_KW => "`fun`",
            SyntaxKind::IF_KW => "`if`",
            SyntaxKind::ELSE_KW => "`else`",
            SyntaxKind::LOOP_KW => "`loop`",
            SyntaxKind::VAL_KW => "`val`",
            SyntaxKind::WHILE_KW => "`while`",
            SyntaxKind::RETURN_KW => "`return`",
            SyntaxKind::BREAK_KW => "`break`",
            SyntaxKind::NAME => "identifier",
            SyntaxKind::INT_NUMBER => "int",
            SyntaxKind::FLOAT_NUMBER => "float",
            SyntaxKind::BINARY_OPERATOR => "binary operator",
            SyntaxKind::POSTFIX_OPERATOR => "postfix operator",
            SyntaxKind::PREFIX_OPERATOR => "prefix operator",
            SyntaxKind::UNKNOWN => "unknown",
            SyntaxKind::EOF => "EOF",
            _ => "<syntax node>",
        })
    }
}
